		public static string GetStringForIcon(this eKrangAtHomeSourceIcon icon, bool active)
		{
			return GetStringForIcon((eKrangAtHomeSourceIcon?)icon, active);
		}


		public static string GetStringForIcon(this eKrangAtHomeSourceIcon? icon, bool active)
		{
			if (!icon.HasValue)
				return TRANSPARENT_ICON_SERIAL;

			StringBuilder serial = new StringBuilder();
			serial.Append("si_");

			string iconSpecificSerial;
			if (!s_IconSerials.TryGetValue(icon.Value, out iconSpecificSerial))
				throw new ArgumentOutOfRangeException("icon", String.Format("No mapping of type {0} to string", icon));
			serial.Append(iconSpecificSerial);

			serial.Append(active ? "_black" : "_white");

			return serial.ToString();
		}
